//1. Function declaration
function add(a, b) {
    return a + b;
  }
  
  console.log(add(5, 3));


  //2. Function Expression
  const subtract = function(a, b) {
    return a - b;
  };
  
  console.log(subtract(10, 4));

  
  //3. Arrow Function
  const multiply = (a, b) => a * b;

console.log(multiply(6, 7));

  
  